"""
Type de variables
1. chaîne de caractères (str)
    - Tout le contenu entre guillemets
2. entiers (int)
3. nombre à virgule flottante (float)
    - nombres avec un point décimal
"""
print(10 + 20)  # 30
print(10 - 20)  # -10
print(10 * 20)  # 200
print(10 / 20)  # 0.5               10 / 20 = 0.5
print(9 // 2)   # 4                 9 / 2 = 4 ......1
print(9 % 2)    # 1
print(2 ** 3)   # 2 x 2 x 2 = 8
print(3 ** 2)   # 3 x 3 = 9
print(2 ** 4)   # 2 x 2 x 2 x 2 = 16













